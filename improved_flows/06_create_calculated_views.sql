-- 5. Í≥†Í∞ù Ïú†ÏûÖÍ≤ΩÎ°ú Î∂ÑÏÑù Î∑∞ (Í∏∞Ï°¥ Ïú†ÏßÄ + Ìå®ÌÇ§ÏßÄ Ï†ïÎ≥¥ Ï∂îÍ∞Ä)
